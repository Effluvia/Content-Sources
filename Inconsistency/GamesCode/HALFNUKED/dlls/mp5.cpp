class CMP5AmmoGrenade : public CBasePlayerAmmo
{
	void Spawn( void )
	{ 
		Precache( );
		SET_MODEL(ENT(pev), "models/w_ARgrenade.mdl");
		CheckMap();
		CBasePlayerAmmo::Spawn( );
	}
	void Precache( void )
	{
		PRECACHE_MODEL ("models/w_ARgrenade.mdl");
		PRECACHE_SOUND("items/9mmclip1.wav");
	}
	void CheckMap()
	{
		#define MAPNAME_LENGHT (unsigned)strlen(MapName)
		char* MapName = (char*)STRING(gpGlobals->mapname);
		if (MAPNAME_LENGHT == 5)
		{
			if ((tolower(MapName[0]) == 't') && (MapName[1] == '0') && (tolower(MapName[2]) == 'a') && (MapName[3] == '0') && (tolower(MapName[4]) == 'b'))
			{
				SpawnItem("weapon_handgrenade", pev->origin, pev->angles);
				UTIL_Remove(this);
			}
		}
	}
	CBaseEntity* SpawnItem(char *pszItemName, const Vector &vecPos, const Vector &vecAng)
	{
		if (!pszItemName)
		{
			ALERT(at_console, "DropItem() - No item name!\n");
			return NULL;
		}
		CBaseEntity *pItem = CBaseEntity::Create(pszItemName, vecPos, vecAng, edict());
		if (pItem)
			return pItem;
		else
		{
			ALERT(at_console, "DropItem() - Didn't create!\n");
			return FALSE;
		}
	}
	BOOL AddAmmo( CBaseEntity *pOther ) 
	{ 
		int bResult = (pOther->GiveAmmo( AMMO_M203BOX_GIVE, "ARgrenades", M203_GRENADE_MAX_CARRY ) != -1);

		if (bResult)
		{
			EMIT_SOUND(ENT(pev), CHAN_ITEM, "items/9mmclip1.wav", 1, ATTN_NORM);
			pOther->pev->iuser4 = T_PICKEDUPAMMO;
		}
		return bResult;
	}
};

