void CShotgunA::Spawn( )
{
	Precache( );
	m_iId = WEAPON_SHOTGUNA;
	SET_MODEL(ENT(pev), "models/w_autoshotgun.mdl");

	m_iDefaultAmmo = SHOTGUNA_DEFAULT_GIVE;

	FallInit();// get ready to fall
}


